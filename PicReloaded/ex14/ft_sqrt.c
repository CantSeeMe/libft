int		ft_sqrt(int nb)
{
	int sqrt;
	int a;

	sqrt = 2;
	if (nb == 1 || nb == 0)
		return (nb);
	if (nb < 0)
		return (0);
	while ((a = sqrt * sqrt) < nb)
		sqrt++;
	return (a == nb ? sqrt : 0);
}
