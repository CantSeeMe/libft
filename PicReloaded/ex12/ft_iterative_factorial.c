int		ft_iterative_factorial(int nb)
{
	int a;

	a = 1;
	if (nb == 0 || nb == 1)
		return (a);
	else if (nb >= 13 || nb < 0)
		return (0);
	else
		while (nb > 1)
			a *= nb--;
	return (a);
}
